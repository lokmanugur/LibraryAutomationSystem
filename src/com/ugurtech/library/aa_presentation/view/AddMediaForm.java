    private void fileChooser(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fileChooser

        jFileChooser1.setAcceptAllFileFilterUsed(false);
        FileNameExtensionFilter filter = new FileNameExtensionFilter("PNG, JPG", "png", "jpg", "jpeg");
        jFileChooser1.addChoosableFileFilter(filter);

        int returnValue = jFileChooser1.showOpenDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            selectedFile = jFileChooser1.getSelectedFile();
            dosya.setText("...\\" + selectedFile.getName());
        }
    }//GEN-LAST:event_fileChooser
    private void save() {
        try {
            Integer beginindex;
            Integer endindex;
            String imgName;
            if(dosya.getText().equals("")){
                imgName="";
            }else{
                imgName = System.currentTimeMillis()+selectedFile.getName().substring(selectedFile.getName().indexOf("."),selectedFile.getName().length());
            }
            if(!dosya.getText().equals("")){
                try {
                    File nowImageFile = new File(System.getProperty("user.dir") + "/src/foodImages/" + imgName);
                    Files.copy(jFileChooser1.getSelectedFile().toPath(), nowImageFile.toPath());
                    BufferedImage img = resize(ImageIO.read(nowImageFile), 48, 48);
                    ImageIO.write(img, "jpg", nowImageFile);
                }catch (IOException ex){
                    Logger.getLogger(AddMediaForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            jFileChooser1.removeAll();
            dosya.setText("");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Database error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(UserForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

