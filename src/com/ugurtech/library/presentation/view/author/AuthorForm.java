    @Override
    public void initController() {
        buttonCancel.addActionListener((java.awt.event.ActionEvent evt) -> {
            clearAllFields();
            authorModel=null;
            this.dispose();
        });

        buttonSave.addActionListener((java.awt.event.ActionEvent evt) -> {
            if(Objects.isNull(authorModel)){
                add(formToModel(new AuthorModel()));
            }else{
                update(formToModel(authorModel));
                authorModel=null;
            }
            clearAllFields();
            this.dispose();
        });
    }

    public AuthorModel formToModel(AuthorModel authorModel) {
        if (textFieldFirstName.getText().equals("") && textFieldLastName.getText().equals("")) {
            UserInfoMessages.getInstance().showInfoMessages(setLanguage("authorcontroller.add.leaveblank.caution"));
            return null;
        } else {
            int authorId = authorModel == null ? 0 : authorModel.getAuthorId();
            authorModel = new AuthorModel();
            authorModel.setAuthorId(authorId);
            authorModel.setFirstName(textFieldFirstName.getText());
            authorModel.setLastName(textFieldLastName.getText());
            authorModel.setBirthDate(birthDateChooser.getDateEditor().getDate() == null ? 0 : birthDateChooser.getDateEditor().getDate().getTime());
            return authorModel;
        }
    }
    
    public void modelToForm(AuthorModel authorModel) {
        textFieldFirstName.setText(authorModel.getFirstName());
        textFieldLastName.setText(authorModel.getLastName());
        birthDateChooser.setDate(new Date(authorModel.getBirthDate()));
        this.authorModel = authorModel;
    }

    public void clearAllFields() {
        textFieldFirstName.setText(null);
        textFieldLastName.setText(null);
        birthDateChooser.setDate(null);
    }

    private void setLanguage() {
        setTitle(setLanguage("authorform.title"));
        labelFirstName.setText(setLanguage("authorform.panelsave.labelfirstname"));
        labelLastName.setText(setLanguage("authorform.panelsave.labellastname"));
        labelBirthDate.setText(setLanguage("authorform.panelsave.labelbirthdate"));
        buttonSave.setText(setLanguage("authorform.panelsave.savebutton"));
        buttonCancel.setText(setLanguage("authorform.panelsave.cancelbutton"));
    }
