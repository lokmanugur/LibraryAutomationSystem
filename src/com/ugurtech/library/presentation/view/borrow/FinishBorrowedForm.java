    public void update() {
        if ("".equals(textFieldReadPages.getText())) {
            UserInfoMessages.getInstance().emptyField("Read Pages", labelReadPages);
        } else {
            personBookModel.setReadPage(Integer.valueOf(textFieldReadPages.getText()));
            update(personBookModel);
        }
    }

